package methodparam.sums;

public class PlusMinusDouble {
    private final double plus;
    private final double minus;

    public PlusMinusDouble(double plus, double minus) {
        this.plus = plus;
        this.minus = minus;
    }

    public double getPlus() {
        return plus;
    }

    public double getMinus() {
        return minus;
    }
}
