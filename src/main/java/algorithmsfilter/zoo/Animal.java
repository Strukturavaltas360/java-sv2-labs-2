package algorithmsfilter.zoo;

public class Animal {
    private String name;
    private int numberOfLegs;

    public String getName() {
        return name;
    }

    public int getNumberOfLegs() {
        return numberOfLegs;
    }

    public Animal(String name, int numberOfLegs) {
        this.name = name;
        this.numberOfLegs = numberOfLegs;
    }
}
